package com.unconditional;

public class Switch {

	public static void main(String[] args) {

		int grade;
		grade = 95;

		switch (grade) {
		case 45:
			System.out.println("fail");
			break;
		case 75:
			System.out.println("pass");
			break;
			default:
				System.out.println("first class");

		}

	}

}
